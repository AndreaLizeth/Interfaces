    private void cmbBoxFiguraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbBoxFiguraActionPerformed
        // TODO add your handling code here:
       filtrarFigura = (String)cmbBoxFigura.getSelectedItem();
            if(filtrarFigura.equals("Cuadrado")){
                PanelRectangulo.setVisible(false);
                PanelTriangulo.setVisible(false);
                PanelCirculo.setVisible(false);
                PanelCuadrado.setVisible(true);
            }
            
            if(filtrarFigura.equals("Rectangulo")){
                PanelRectangulo.setVisible(true);
                PanelTriangulo.setVisible(false);
                PanelCirculo.setVisible(false);
                PanelCuadrado.setVisible(false);
            }
            if(filtrarFigura.equals("Triangulo")){
                PanelRectangulo.setVisible(false);
                PanelTriangulo.setVisible(true);
                PanelCirculo.setVisible(false);
                PanelCuadrado.setVisible(false);
            }
            if(filtrarFigura.equals("Circulo")){
                PanelRectangulo.setVisible(false);
                PanelTriangulo.setVisible(false);
                PanelCirculo.setVisible(true);
                PanelCuadrado.setVisible(false);
            }
            
            
    }//GEN-LAST:event_cmbBoxFiguraActionPerformed

